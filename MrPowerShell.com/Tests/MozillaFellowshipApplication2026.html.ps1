$totalChars = 0
$maxChars =0 
foreach ($section in $sections.Keys) {
    if (-not $sections[$section]) {
        Write-Host "missing: $section" -ForegroundColor Yellow
        continue
    }
    if ($section -match '(?<chars>[\d,]+) char') {
        $charCount = $matches.chars -replace ',' -as [int]
        $maxChars += $charCount
        $totalChars += $sections[$section].Length
        if ($sections[$section].Length -le $charCount) {
            Write-Host ($charCount - $sections[$section].Length) -ForegroundColor Green
            Write-Host $section -ForegroundColor Green

            Write-Host $sections[$section]

            "<blockquote>$([Web.HttpUtility]::HtmlEncode($section))</blockquote>"

            ConvertFrom-Markdown -InputObject $sections[$section] | 
                Select-Object -Expand Html
            
        } else {
            Write-Host ($charCount - $sections[$section].Length) -ForegroundColor Red
            Write-Host $section -ForegroundColor Red

            "<blockquote>$([Web.HttpUtility]::HtmlEncode($section))</blockquote>"

            ConvertFrom-Markdown -InputObject $sections[$section] | 
                Select-Object -Expand Html

            Write-Host $sections[$section]
            
        }
    }
}

Write-Host "Total Characters $totalChars"
Write-Host "Max Characters $maxChars"